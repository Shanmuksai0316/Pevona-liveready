/**
 * Get property images by slug or title
 */
export function getPropertyImages(slug: string, title?: string): string[] {
  // Try exact slug match first
  if (PROPERTY_IMAGE_MAPPING[slug]) {
    return PROPERTY_IMAGE_MAPPING[slug];
  }

  // Try slug variations
  const slugLower = slug.toLowerCase();
  for (const [key, images] of Object.entries(PROPERTY_IMAGE_MAPPING)) {
    if (slugLower.includes(key.toLowerCase()) || key.toLowerCase().includes(slugLower)) {
      return images;
    }
  }

  // Try title matching
  if (title) {
    const titleLower = title.toLowerCase();
    for (const [key, images] of Object.entries(PROPERTY_IMAGE_MAPPING)) {
      if (titleLower.includes(key.toLowerCase())) {
        return images;
      }
    }
  }

  return [];
}


